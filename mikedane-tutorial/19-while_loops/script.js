alert("While Loops & Do While Loops!");

// creating a while loop
document.write("Creating a while loop: <br>");

index = 1;

while(index <= 5){
    document.write(index + "<br>");
    index++;
}

document.write("<br><br>");

// creating a do loop
document.write("Creating a do loop: <br>");

index = 1;

do{
  document.write(index + "<br>");
  index++;
}while(index <= 5);

